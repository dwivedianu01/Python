# DataType str
x = "Hi Techies"
print("Type of x: ", type(x))
 
# DataType int
x = 100
print("Type of x: ", type(x))

 
# DataType float
x = 40.5
print("Type of x: ", type(x))

# DataType list
x = ["techno2", "for", "techno2"]
print("Type of x: ", type(x))

# DataType tuple
x = ("techno2", "for", "techno2")
print("Type of x: ", type(x))

# DataType range
x = range(33)
print("Type of x: ", type(x))

 
# DataType dict
x = {"name": "Anupam", "age": 24}
print("Type of x: ", type(x))

# DataType set
x = {"techno2", "for", "techno2"}
print("Type of x: ", type(x))

 
# DataType frozenset
x = frozenset({"techno2", "for", "techno2"})
print("Type of x: ", type(x))

 
# DataType complex
x = 3j
print("Type of x: ", type(x))

 
# DataType memoryview
x = memoryview(bytes(6))
print("Type of x: ", type(x))

 
# DataType NoneType
x = None
print("Type of x: ", type(x))


# DataType bool
x = True
print("Type of x: ", type(x))

 
# DataType bytes
x = b"techno2"
print("Type of x: ", type(x))

 
# DataType bytearray
x = bytearray(4)
print("Type of x: ", type(x))

